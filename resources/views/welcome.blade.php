    <div class="p-5">
        <p>Es un instrumento de pago, mediante el cual El BEA (Como Emisor) obrando por solicitud y conformidad con las instrucciones de un cliente (ordenante) debe hacer un pago a un tercero (beneficiario) contra la entrega de los documentos exigidos, siempre y cuando se cumplan los términos y condiciones de crédito. En otras palabras, es un compromiso escrito asumido por El BEA de efectuar el pago al vendedor a su solicitud y de acuerdo con las instrucciones del comprador hasta la suma de dinero Auricoin (RIC) indicada, dentro de determinado tiempo y contra entrega de los documentos indicados. Llamada también “Crédito Comercial”,” Crédito Documentario”, y en algunas ocasiones simplemente crédito. <br><br>
            Toda carta de crédito tiene su origen en un contrato de compra-venta de mercancías (aunque puede originarse en la prestación de servicios). <br><br>
            En resumen, las cartas de crédito son: una promesa de que El BEA va a pagar una cierta suma de dinero Auricoin (RIC); se paga contra la presentación de documentos que certifican el cumplimiento del contrato suscrito entre las partes, el del vendedor en entregar la mercancía y el comprador en pagarla; su plazo para hacer uso de la carta de crédito esta ya pre-establecido. Vencido el plazo, el beneficiario de la carta de crédito no puede girar sobre esta. <br><br>
            El contrato de crédito es el que regula la relación entre El BEA como emisor, ordenante y beneficiario. En este contrato se definen las condiciones bajo las cuales El BEA está dispuesto a emitir cartas de crédito por cuenta del ordenante. En este contrato, como en todo contrato de crédito, se crea una obligación de que El BEA  pone a disposición de su cliente (ordenante del crédito) un crédito por un plazo determinado y bajo condiciones determinadas. <br><br>
            El contrato de crédito no es un pre-contrato ni una promesa de contratar, sino por el contrario, un contrato vinculante para El BEA, es precisamente tener a favor del cliente una disponibilidad determinada para emitir las cartas de crédito aprobadas en el contrato, siendo el objeto mismo del contrato la existencia de la disponibilidad de crédito a favor del cliente y no propiamente la carta de crédito.</p>
