    <div class="p-5">
        <p>
            Directa <br>
            En este caso los pagos solo se pueden realizar a los beneficiarios nombrados en el documento y no pueden intervenir terceros, ya sean personas u otras instituciones. <br><br>

            Irrevocable <br>
            Estas cartas no pueden ser revocadas sin el consentimiento de los vendedores por ninguna parte.  En este caso los pagos solo se pueden realizar a los beneficiarios nombrados en el documento y no pueden intervenir terceros, ya sean personas u otras instituciones.<br><br>

            Revocable <br>
            Esta puede ser revocable sin previo aviso por el comprador, ya que se realizó el acuerdo previo en el contrato entre el vendedor y el comprador.<br><br>

            Transferible <br>
            Esta permite al beneficiario asignar a alguien más todo el crédito disponible, por lo que se crea un segundo beneficiario y en el contrato se refiere entonces a la persona interesada como primer beneficiario.<br><br>

            Standby <br>
            Se trata de una opción más flexible, tanto para el comprador como para quien vende, ya que se asegura que el pago se realice a pesar de que suceda algún tipo de imprevisto.  Una vez que el beneficiario comprueba que no se le cumplió, ésta se hace efectiva.<br><br>

            Importación y exportación <br>
            También llamadas comerciales, son utilizadas para el cumplimiento de tratados internacionales.  En este sentido, si el exportador expide la carta, se trata de una exportación y si la expide el importador, se trata de una importación.<br><br>

            Rotativa <br>
            Se utilizan cuando se tiene planeado realizar varios pagos o negocios continuamente, pero tomando en cuenta que al establecerse las condiciones también se establecen los lapsos que tienen que considerar las personas involucradas.<br><br>

            A la vista <br>
            En este caso los pagos se realizan justo después de haber presentado la documentación pertinente y normalmente en un plazo no mayor a 12 horas.<br><br>

            Back to back <br>
            Se involucran dos cartas.  La primera establece expedir una segunda, donde el comprador presenta el segundo contrato de crédito que, además, fue solicitado por instrucciones de la persona que pidió el primer crédito.<br><br>

            Cláusula roja <br>
            Llevan este nombre porque suelen mostrarse con este color en el documento para dejar claro que se solicita un adelanto en el compromiso.  Las personas favorecidas pueden recibir fondos o mercancía desde antes, siempre y cuando así se marque en la cláusula roja.  Lo recibido como un adelanto se deduce al final del crédito total.<br><br>

            Diferido <br>
            En este caso, a pesar de presentar los documentos que acreditan el pago, éste no se realiza inmediatamente y se debe cumplir el período acordado desde el principio en el contrato. <br><br>
        </p>
